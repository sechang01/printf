#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*s1;

	if (!(s1 = (char *)malloc(sizeof(char) * (size + 1))))
		return (0);
	ft_bzero(s1, size + 1);
	return (s1);
}
